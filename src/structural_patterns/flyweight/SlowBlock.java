package structural_patterns.flyweight;

public class SlowBlock {

    int x;
    int y;
    String color;
    float size;
    String name;

    public SlowBlock(int x, int y, String color, float size, String name) {
        this.x = x;
        this.y = y;
        this.color = color;
        this.size = size;
        this.name = name;
    }

}
